package flat.graphics.svg;

public class BezierCurve {
    public float cx1, cy1, cx2, cy2, x1, y1, x2, y2;

    public float getCx1() {
        return cx1;
    }

    public void setCx1(float cx1) {
        this.cx1 = cx1;
    }

    public float getCy1() {
        return cy1;
    }

    public void setCy1(float cy1) {
        this.cy1 = cy1;
    }

    public float getCx2() {
        return cx2;
    }

    public void setCx2(float cx2) {
        this.cx2 = cx2;
    }

    public float getCy2() {
        return cy2;
    }

    public void setCy2(float cy2) {
        this.cy2 = cy2;
    }

    public float getX1() {
        return x1;
    }

    public void setX1(float x1) {
        this.x1 = x1;
    }

    public float getY1() {
        return y1;
    }

    public void setY1(float y1) {
        this.y1 = y1;
    }

    public float getX2() {
        return x2;
    }

    public void setX2(float x2) {
        this.x2 = x2;
    }

    public float getY2() {
        return y2;
    }

    public void setY2(float y2) {
        this.y2 = y2;
    }
}
