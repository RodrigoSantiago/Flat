package flat.graphics.svg;

public class QuadCurve {
    float x1, y1, x2, y2, cx,  cy;

    public float getX1() {
        return x1;
    }

    public float getY1() {
        return y1;
    }

    public float getX2() {
        return x2;
    }

    public float getY2() {
        return y2;
    }

    public float getCx() {
        return cx;
    }

    public float getCy() {
        return cy;
    }
}
